
class Baliza:
    def __init__(self, id, largura, altura):
        self.id = id
        self.largura  = largura
        self.altura = altura



    def set_id(self, valor):
        self.id = valor

    def get_id(self):
        return self.id

    def set_largura(self, valor):
        self.largura = valor

    def get_largura(self):
        return self.largura

    def set_altura(self, valor):
        self.altura = valor

    def get_altura(self):
        return self.altura

